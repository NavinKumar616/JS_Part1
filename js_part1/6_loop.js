// loop
// 1.for loop
// 2.while loop
// 3.do while loop

// part 1
for (var i = 0; i <= 10; i++) {
    console.log("navin");
}
//  part 2
// dout -> ${}
for (var i = 1; i <= 10; i++) {
    console.log(`2 X ${i} = ${2 * i}`);
}
// part 3
for (var i = 1; i <= 10; i++) {
    console.log("2 X" + i + "=" + 2 * i);
}

// part 4
// while loop
var j = 1;
while (j <= 10) {
    console.log("2 X" + j + "=" + 2 * j);
    j++;
}

// part 5
// do while

do {
    console.log("2 X" + j + "=" + 2 * j);
    j++;
} while (j <= 10);
